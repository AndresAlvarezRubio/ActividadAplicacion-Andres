//Desarrollar lógica detrás de ++a, a++, --a, a--

console.log("");
console.log("Caso de (a++) -> Primero se enseña la variable, y luego se suma");
let a = 5;
console.log(a++); 
console.log(a);


console.log("");
console.log("Caso de (++b) -> Primero se suma, y luego se enseña la variable");
let b = 5;
console.log(++b);
console.log(b);

console.log("");
console.log("Caso de (c--) -> Primero se enseña la variable, y luego se resta");
let c = 5;
console.log(c--); 
console.log(c);

console.log("");
console.log("Caso de (--d) -> Primero se resta, y luego se enseña la variable");
let d = 5;
console.log(--d);
console.log(d);


